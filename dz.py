s = input("Введите строку :  ")
print("Третий символ строки : ", s[2])
print("Предпоследний символ строки : ", s[-2])
print("Первые пять символов строки : ", s[:5])
print("Строка без последних двух символов : ", s[:-2])
print("Четный индекс : ", s[::2])
print("Нечетный индекс : ", s[1::2])
print("В обратном порядке : ", s[::-1])
print("В обратном порядке через один : ", s[::-2])
print(len(s))
